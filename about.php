    <h1>FAQs</h1>

    <p><strong>Why is my location not exactly where I am right now?</strong></p>

    <p>For sake of speed and device compatibility, Today Weather uses your IP address to locate you. This will almost always be somewhere close to you, but it might be a town or two away depending on how your internet provider works.</p>

    <p>When you tap/click on the &quot;Find Me&quot; button in the top right of the page, you should then get a prompt to allow this site to use your location. At that point, the page will refresh with the weather for your exact location.</p>

    <hr>

    <p><strong>Can I have Today Weather use my GPS automatically?</strong></p>

    <p>As of right now, no. It is something that may be added in the future, but it&#39;s not here yet. In the meantime, you can always bookmark the page that is generated when you tap/click &quot;Find me&quot; because this link will always pull up this exact location. This is most useful if you tend to check the weather from home or from work all the time.</p>

    <hr>

    <p><strong>Can I search for anywhere in the world?</strong></p>

    <p>You can! Today Weather has a smart search engine that can search for just about anything you ask it. Simply tap/click on the current location near the top and search for:</p>

    <ul>
    	<li>City</li>
    	<li>State/Province</li>
    	<li>Nation</li>
    	<li>Address</li>
    	<li>Postal code</li>
    	<li>Latitude &amp; Longetude</li>
    </ul>

    <hr>

    <p><strong>How accurate is this weather data?</strong></p>

    <p>Very accurate! This site is built on the <a href="https://developer.forecast.io">Dark Sky API</a>, which is regarded as one of the best weather sources out there. </p>

    <hr>

    <p><strong>Radar?</strong></p>

    <p>Not yet. Radar is on the road map as something we would like to add very soon, though.</p>

    <hr>

    <p><strong>Why do some places show temperatures in Celsius and others in Fahrenheit?</strong></p>

    <p>Units of measure will always appear in whatever format is native to that region. There are only 5 nations that use the imperial system of measurement, while everyone else uses the beautiful metric system.</p>

    <p>We know there are people who may want to see all temperatures in Celsius while they prefer thier distance speeds to be in miles, and this won't work for you yet. User settings to control these are on the road map though.</p>

    <hr>

